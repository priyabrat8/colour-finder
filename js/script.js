const showColor = () => {
    const value = (input.value).toLocaleLowerCase();
    colorDiv.innerHTML = " "
    for (let i = 0; i < colors.length; i++) {
        const color = (colors[i].toLocaleLowerCase());
        if (value !== "") {
            if (color.includes(value)) {
                const newDiv = `<div class="color" style="background-color: ${color==="black" ? "black ; color: white"  : color}">${color}</div>`
                colorDiv.insertAdjacentHTML("afterbegin",newDiv)
            }     
        }
        else {
            return
        }
    } 
}

button.addEventListener('click', (e) => {
    e.preventDefault();
    showColor()
})
